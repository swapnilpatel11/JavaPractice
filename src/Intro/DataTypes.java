package Intro;

public class DataTypes {

	public static void main(String[] args) {
		//Data types in java
		//integer,double,char,boolean primitive data types
		//1. integer
		int i = 10;
		i = 20; //latest value will be given to i 
				
		//2.double
		double a = 10.2d;
		
		//3.char: only one digit value
		char c = 'A';
		char c2 = '1';  //its not integer 
		
		//4.boolean
		boolean b = true;
		
		//5.String is not datatype its a class
		//we can use as datatype values
		String s = "Hello Swapnil";
		String n = "1234";
		
	
	}

}
